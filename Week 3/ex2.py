# =============Arithmetic Comparisons=============

# ----------Six different arithmetic comparisons-------------
print("Comparisons")
print("================")
print(7 > 3)
print(7 < 3)
print(7 >= 3)
print(7 <= 3)
print(7 == 3)
print(7 != 3)
print("")

# -----------Using comparisons to get a boolean value---------
print("Comparing Variables")
print("===============")
num1 = 7.3
num2 = 8.6

greater = num1 > num2
print(greater)
print("")

# ------------== and != are also useful for non-numeric types----------
print("Comparing Strings")
print("=================")
name = "Himal"
equal = name != "Scott" # name is not equal to 'Scott'
print(equal)

# Beware of = and == confusion
equal = name == "Scott" # name is equal to 'Scott'
print(equal)

