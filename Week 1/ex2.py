# =======Simple Expressions========

# ----Unary + and -    -----
print("Unary Operators")
print(+3)
print(-5)
print(+7.86)
print(-3348.63)
print("")

# -----Simple Arithmetic-----
print("Addition and Substraction")
print(1 + 2)
print(48 - 89)
print(3.45 + 2.7)
print(87.3384 - 12.35)
print(3 + 6.7)
print(9.8 - 4)
print("")

print("Multiplication")
print(3 * 4)
print(7.8 * 27.54)
print(7 * 8.3)
print("")

print("Division") # Division always gives back a float number
print(8 / 2)
print(3 / 2)
print(7.538 / 14.3)
print(8 // 2) # // Means integer divide, it returns an integer
print(3 // 2)
print(7.538 // 14.3)

print("Exponentiation")
print(3 ** 2) # 3 to the power of 2
print(5 ** 2)
print(32.6 ** 7)
print(9 ** 0.5)
