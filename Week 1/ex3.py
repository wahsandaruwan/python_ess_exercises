# =======Compound Expressions=======

# ------Expressions can include multiple operations--------
print("Compound Expressions")
print(3 + 5 + 7 + 27)
print(18 - 6 + 4)
print("")

# -------Operator precedence defines how expressions are evaluated--------
print("Operator Precedence")
print(7 + 3 * 5)
print(5.5 * 6 // 2 + 8)
print(-3 ** 2)  # 3 square first and then set minus (Because ** has higer precedence)
print("")

# ---------Use parenthesis to change evaluation order---------
print("Grouping with parenthesis")
print((7 + 3) * 5)
print(5.5 * ((6 // 2) + 8))
print((-3) ** 2)
